//////////////////////////////////////////////////////////////////////
// D2Merc.h: interface of the CD2Merc class.
//////////////////////////////////////////////////////////////////////

#ifndef __D2MERC_H__
#define __D2MERC_H__

#include "D2Monster.h"

///////////////////////////////////////////////////////////////////////
// CD2Merc Class Definition
///////////////////////////////////////////////////////////////////////
class CD2Merc : public CD2Monster
{
public:

	///////////////////////////////////////////////////////////////////////
	// Constructor(s) & destructor
	///////////////////////////////////////////////////////////////////////
	CD2Merc();
	virtual ~CD2Merc();

	///////////////////////////////////////////////////////////////////////
	// Client Entries
	///////////////////////////////////////////////////////////////////////
	virtual void OnGameJoin(DWORD dwPlayerID, const WORD* pPlayerXAddr, const WORD* pPlayerYAddr);
	virtual void OnGameLeave();
	virtual void OnGamePacketBeforeReceived(const BYTE* aPacket, DWORD aLen);
	virtual void OnGamePacketBeforeSent(const BYTE* aPacket, DWORD aLen);
	virtual void OnGameTimerTick();

	///////////////////////////////////////////////////////////////////////
	// Public Attributes
	///////////////////////////////////////////////////////////////////////

	///////////////////////////////////////////////////////////////////////
	// Public Operations
	///////////////////////////////////////////////////////////////////////
	void InitAttributes(); // Clean all attributes
	virtual BOOL SetFingerprinter(LPCD2OBJFINGERPRINTER lpFingerprinter) { return FALSE; } // Blocked

protected:

	///////////////////////////////////////////////////////////////////////
	// Overrides
	///////////////////////////////////////////////////////////////////////

	///////////////////////////////////////////////////////////////////////
	// Protected Operations
	///////////////////////////////////////////////////////////////////////

	///////////////////////////////////////////////////////////////////////
	// Protected Member Data
	///////////////////////////////////////////////////////////////////////
	DWORD m_dwPlayerID; // The player who owns this merc
	const WORD* m_pPlayerXAddr; // The player's position X address
	const WORD* m_pPlayerYAddr; // The player's position X address

private:
	virtual void OnGameJoin() {} // Blocked
};

#endif // __D2MERC_H__